function product(id, name, trademark, description, price, img, internalMemory, ram, processor, soVersion, mainCam, frontalCam) {
    this.id = id;
    this.name = name;
    this.trademark = trademark;
    this.description = description;
    this.price = price;
    this.img = img;
    this.internalMemory = internalMemory;
    this.ram = ram;
    this.processor = processor;
    this.soVersion = soVersion;
    this.mainCam = mainCam;
    this.frontalCam = frontalCam;
}
